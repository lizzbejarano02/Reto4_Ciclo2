    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
       //1. Consultamos mediante la funcion getText() el valor que el usuario
       //ingreso en el campo de usuario y de contraseña
       String username = txtUsuario.getText();
       String password = txtPassword.getText();
       
       System.out.println("username: " + username + ", password: " + password);
       //2. Validar si alguno de los dos campos esta vacio
       if(username.isEmpty() || password.isEmpty()){
          JOptionPane.showMessageDialog(this, "Hay campos sin diligenciar.", "Login", JOptionPane.WARNING_MESSAGE);
       }else{
           //3. Validar el escenario en el que la información corresponde a los datos de inicio de sesión
           if(username.equals("MisionTIC") && password.equals("Ciclo2")){
               JOptionPane.showMessageDialog(this, "Bienvenido al sistema" );
               //4.Crear una instancia de la vista UserMenú
               UserMenu usermenu = new UserMenu();
               //5. Hacer Visible la vista mediante el metodo setVisible()
               usermenu.setVisible(true);
               //6. Eliminar la vista de Login
               this.dispose();
           }else{
               JOptionPane.showMessageDialog(this,"Error en los datos de inicio de sesión","Login", JOptionPane.ERROR_MESSAGE );
           }
       }
    }//GEN-LAST:event_btnLoginActionPerformed
    
